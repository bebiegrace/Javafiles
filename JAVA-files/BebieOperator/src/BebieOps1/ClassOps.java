package BebieOps1;

public class ClassOps {
    public double decrement (double a)
    {
        return (--a);
    }
    public double increment (double a)
    {
        return (++a);
    }public double multicomp (double a, double b)
    {
        return (a *=b );
    }public double divicomp (double a, double b)
    {
        return (a /= b);
    }
    
}
